package mainPackage;

public class EvenAndOddArray {

	public static void main(String[] args) {
		
		int[][] num=new int[3][4];
		
		
		//1 array or 1 row
		num[0][0]=1;
		num[0][1]=2;
		num[0][2]=3;
		num[0][3]=4;
		
		//2 array or 2 row
		num[1][0]=10;
		num[1][1]=22;
		num[1][2]=33;
		num[1][3]=43;
		
		//3 array or 3 row
		num[2][0]=110;
		num[2][1]=201;
		num[2][2]=322;
		num[2][3]=411;
		
		
		
		
		
	}

}
